package com.pmp.hrsrc.entity;

import java.io.Serializable;

public class UserRole implements Serializable {

    public int id;
    public int uid;
    public int rid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }
}
